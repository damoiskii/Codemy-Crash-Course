void main() {
  // Data Types
  // Numbers, Strings, Booleans, Lists, Maps, Sets, Dynamic

  // Strings
  String firstName = "Moi";
  String lastName = "My";
  print("Firstname: " + firstName);
  print("Lastname: $lastName");

  // Integers
  int age = 30;
  print("Age: $age");

  // Doubles
  double gpa = 3.66;
  print("GPA: $gpa");

  // Booleans
  bool found = false;
  print("Found: $found");

  // Dynamic
  dynamic text = "Hello world!";
  print("Dynamic: $text");

  text = 10;
  print("Dynamic [after update]: $text");
}
